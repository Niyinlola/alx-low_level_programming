/**
 * op_add - a function that add up a and b
 * @a: first input
 * @b: second input
 * Return: @a and @b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that collects the difference of a and b
 * @a: first input
 * @b: second input
 * Return: a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiplies a and b
 * @a: first input
 * @b: second input
 * Return: a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - a function that divides a by b
 * @a: first input
 * @b: second input
 * Return: results of @a and @b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that collects the reminder of
 * the division between @a and @b
 * @a: first input
 * @b: second input
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
